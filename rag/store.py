"""
rag/store.py

Schema-aware vector retrieval using ChromaDB + nomic-embed-text.

On this hardware the RAG layer serves two purposes:
1. Provides Llama 3.1 8B with precise field context for unusual queries
2. Helps identify which fields are most relevant for aggregation selection

The embedding model (nomic-embed-text) runs on CPU via Ollama —
it doesn't need the GPU, leaving VRAM for Llama 3.1 8B.
"""

